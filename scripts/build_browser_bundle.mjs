#!/usr/bin/env node

import { mkdirSync } from "node:fs";
import { resolve } from "node:path";
import { build } from "esbuild";

const outdir = resolve("dist");
mkdirSync(outdir, { recursive: true });

await build({
  entryPoints: [resolve("src/runtime.js")],
  outfile: resolve(outdir, "runtime.browser.js"),
  bundle: true,
  format: "esm",
  platform: "browser",
  target: "es2022",
  sourcemap: false,
  legalComments: "none",
  banner: {
    js: "// Rangefind browser runtime bundle. Generated by npm run build:browser."
  }
});
